// Regenerates dist/_ds_all.js — the design-sync bundle entry that exposes the
// FULL @godxjp/ui surface (all 8 category barrels + a few catalogued extras) on
// window.<global>, so the claude.ai/design agent can compose every primitive and
// its sub-parts (Card + CardContent, Dialog + DialogContent, …).
//
// Why this exists: the package's published `.` entry (dist/index.js) is a thin
// curated "admin" surface (~37 exports) and is MISSING the core primitives
// (Button, Card, Input, Select, Calendar, DataTable, …). The real public API is
// subpath-based (@godxjp/ui/general, /data-display, /data-entry, …). This script
// stitches those barrels back into one IIFE-able entry.
//
// dist/ is rebuilt by `pnpm build`, which wipes this file — so it runs as part of
// cfg.buildCmd ("pnpm build && node .design-sync/gen-aggregate.mjs"). Idempotent.
//
// The CARD set (which components get preview cards) is curated separately to the
// 92 catalogued components via cfg.componentSrcMap in .design-sync/config.json —
// this file only controls what the bundle EXPORTS, not what gets a card.

import { writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const dist = join(repoRoot, 'dist');

const barrels = [
  'general', 'layout', 'data-display', 'data-entry',
  'feedback', 'navigation', 'charts', 'query',
];

for (const b of barrels) {
  const f = join(dist, 'components', b, 'index.js');
  if (!existsSync(f)) {
    console.error(`[gen-aggregate] missing barrel: dist/components/${b}/index.js — did \`pnpm build\` run?`);
    process.exit(1);
  }
}

const out = [
  '// AUTO-GENERATED by .design-sync/gen-aggregate.mjs — do not edit. Full window.<global> surface.',
  ...barrels.map((b) => `export * from "./components/${b}/index.js";`),
  // UploadCropDialog ships its own file but isn't re-exported from the data-entry barrel.
  'export { UploadCropDialog } from "./components/data-entry/upload-crop-dialog.js";',
  // AppProvider (i18n/theme context wrapper) + formatDate are catalogued, but live under /app.
  'export { AppProvider, formatDate } from "./app/index.js";',
  '',
].join('\n');

writeFileSync(join(dist, '_ds_all.js'), out);
console.error(`[gen-aggregate] wrote dist/_ds_all.js (${barrels.length} barrels + 3 extras)`);
